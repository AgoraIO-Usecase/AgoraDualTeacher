static void draw_num(IMAGE* img, const int stride, const int height, const char* font, const int x,
                     const int y) {
  int i, j;
  for (j = 0; j < FONT_ZOOM * FONT_HEIGHT && y + j < height; j++)
    for (i = 0; i < FONT_ZOOM * FONT_WIDTH && x + i < stride; i++)
      if (font[(j / FONT_ZOOM) * FONT_WIDTH + (i / FONT_ZOOM)]) {
        int offset = (y + j) * stride + (x + i);
        int offset2 = ((y + j) / 2) * (stride / 2) + ((x + i) / 2);
        img->y[offset] = 0;
        img->u[offset2] = 127;
        img->v[offset2] = 127;
      }
}

#define FONT_BUF_SZ 1024
static void image_printf(IMAGE* img, int edged_width, int height, int x, int y, const char* fmt,
                         ...) {
  va_list args;
  char buf[FONT_BUF_SZ];
  int i;
  va_start(args, fmt);
  vsprintf(buf, fmt, args);
  va_end(args);

  for (i = 0; i < buf[i]; i++) {
    const char* font;

    if (buf[i] >= '!' && buf[i] <= '@')
      font = ascii33[buf[i] - '!'];
    else if (buf[i] >= 'A' && buf[i] <= 'Z')
      font = ascii65[buf[i] - 'A'];
    else if (buf[i] >= '[' && buf[i] <= '`')
      font = ascii91[buf[i] - '['];
    else if (buf[i] >= 'a' && buf[i] <= 'z')
      font = ascii65[buf[i] - 'a'];
    else
      continue;

    draw_num(img, edged_width, height, font, x + i * FONT_ZOOM * (FONT_WIDTH + 1), y);
  }
}

static int frame_count = 0;
void PrintFrameCount(const webrtc::VideoFrame& frame) {
  auto input_image = frame.video_frame_buffer()->ToI420();

  IMAGE image;
  int edged_width = input_image->StrideY();
  int height = input_image->height();
  int x_offset = input_image->width() / 4;
  int y_offset = input_image->height() / 8;
  image.y = const_cast<uint8_t*>(input_image->DataY());
  image.u = const_cast<uint8_t*>(input_image->DataU());
  image.v = const_cast<uint8_t*>(input_image->DataV());
  image_printf(&image, edged_width, height, x_offset, y_offset, "%d", frame_count++);

  if (frame.width() > sizeof(int)) {
    memcpy(image.y, &frame_count, sizeof(frame_count));
  }
}

